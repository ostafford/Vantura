# Solid Queue Configuration
# Rails 8's built-in background job processor using PostgreSQL
#
# Solid Queue uses config/queue.yml for worker configuration
# See config/queue.yml for dispatcher and worker settings
#
# Worker configuration is handled via:
# - config/queue.yml (worker processes, threads, queues)
# - config/environments/*.rb (queue adapter configuration)
#
# To start workers locally: bundle exec rake solid_queue:start

